def _format_money(value):
    try:
        return f"{float(value):,.2f}"
    except Exception:
        return "0.00"


def _safe_text(value):
    value = str(value or "").strip()
    return value if value else "-"


def _format_date(value):
    if not value:
        return "-"

    try:
        if isinstance(value, datetime.datetime):
            return value.strftime("%d %b %Y")
        if isinstance(value, datetime.date):
            return value.strftime("%d %b %Y")

        dt = datetime.datetime.strptime(str(value), "%Y-%m-%d")
        return dt.strftime("%d %b %Y")
    except Exception:
        return str(value)


def _build_address(*parts):
    cleaned = []
    for part in parts:
        part = str(part or "").strip()
        if part:
            cleaned.append(part)
    return ", ".join(cleaned) if cleaned else "-"


def _wrap_text_lines(c, text, max_width, font_name="Helvetica", font_size=9):
    words = str(text or "-").split()
    lines = []
    line = ""

    for word in words:
        test_line = f"{line} {word}".strip()
        if c.stringWidth(test_line, font_name, font_size) <= max_width:
            line = test_line
        else:
            if line:
                lines.append(line)
            line = word

    if line:
        lines.append(line)

    return lines if lines else ["-"]


def _draw_image_keep_ratio(c, img, x, y, max_width, max_height):
    try:
        img_width, img_height = img.getSize()
        ratio = min(max_width / img_width, max_height / img_height)
        draw_width = img_width * ratio
        draw_height = img_height * ratio

        c.drawImage(
            img,
            x,
            y + (max_height - draw_height) / 2,
            width=draw_width,
            height=draw_height,
            mask="auto",
        )
    except Exception as e:
        print(f"[WARN] Failed to draw image: {e}")


